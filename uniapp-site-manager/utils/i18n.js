// 临时使用简化的国际化方案，等待vue-i18n依赖问题解决
const createI18n = (options) => {
  const { locale, fallbackLocale, messages } = options
  
  return {
    global: {
      locale: locale,
      fallbackLocale: fallbackLocale,
      messages: messages,
      t: function(key, params = {}) {
        const keys = key.split('.')
        let value = this.messages[this.locale]
        for (const k of keys) {
          value = value?.[k]
        }
        
        if (!value) return key
        
        // 支持参数插值，如 {minutes} {hours} 等
        if (typeof value === 'string' && Object.keys(params).length > 0) {
          return value.replace(/\{(\w+)\}/g, (match, paramKey) => {
            return params[paramKey] !== undefined ? params[paramKey] : match
          })
        }
        
        return value
      }
    },
    install(app) {
      app.config.globalProperties.$t = this.global.t.bind(this.global)
      app.provide('i18n', this)
    }
  }
}

