// 临时使用简化的国际化方案，等待vue-i18n依赖问题解决
const createI18n = (options) => {
  const { locale, fallbackLocale, messages } = options

  return {
    global: {
      locale: locale,
      fallbackLocale: fallbackLocale,
      messages: messages,
      t: function (key, params = {}) {
        const keys = key.split('.')

        const localesToTry = []
        const addLocale = (loc) => {
          if (!loc) return
          if (localesToTry.includes(loc)) return
          localesToTry.push(loc)
        }

        // 1) 当前语言
        addLocale(this.locale)
        // 2) 印尼语缺失较多，优先回落到英文，避免展示 key（形如 a.b）
        if (this.locale !== 'en') addLocale('en')
        // 3) 兼容配置的 fallbackLocale（可为 string 或数组）
        if (Array.isArray(this.fallbackLocale)) {
          this.fallbackLocale.forEach(addLocale)
        } else {
          addLocale(this.fallbackLocale)
        }
        // 4) 最后兜底中文
        if (this.locale !== 'zh') addLocale('zh')

        let value
        for (const loc of localesToTry) {
          let current = this.messages?.[loc]
          for (const k of keys) {
            current = current?.[k]
          }
          // 空字符串视为缺失，继续回落
          if (current === undefined || current === null || current === '') continue
          value = current
          break
        }

        if (value === undefined || value === null || value === '') return key

        // 支持参数插值，如 {minutes} {hours} 等
        if (typeof value === 'string' && Object.keys(params).length > 0) {
          return value.replace(/\{(\w+)\}/g, (match, paramKey) => {
            return params[paramKey] !== undefined ? params[paramKey] : match
          })
        }

        return value
      }
    },
    install(app) {
      app.config.globalProperties.$t = this.global.t.bind(this.global)
      app.provide('i18n', this)
    }
  }
}

